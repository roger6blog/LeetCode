class Solution(object):
    def isValidSudoku(self, board):
        """
        :type board: List[List[str]]
        :rtype: bool
        """
        from collections import Counter
        def check_sudoku(board, valid_sudoku):
            m = len(board)
            n = len(board[0])
            rotate_board = [[board[j][i] for j in range(n)] for i  in range(m)[::-1]]
            for i in range(m):
                c = Counter(board[i])
                del c["."]
                for k, v in c.items():
                    if v > 1:
                        valid_sudoku = False

            for i in range(m):
                c = Counter(rotate_board[i])
                del c["."]
                for k, v in c.items():
                    if v > 1:
                        valid_sudoku = False

            return valid_sudoku

